#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putchar3(char i, char j, char k)
{
	ft_putchar(i);
	ft_putchar(j);
	ft_putchar(k);
	if (i == '7' && j == '8' && k == '9')
	{
		return ;
	}
	else
	{
		write(1, ",", 1);
		write(1, " ", 1);
	}
}

void	ft_print_comb(void)
{
	char	i;
	char	j;
	char	k;

	i = '0';
	while ('7' >= i)
	{
		j = i + 1;
		while ('8' >= j)
		{
			k = j + 1;
			while ('9' >= k)
			{
				ft_putchar3(i, j, k);
				k++;
			}
			j++;
		}
		i++;
	}
}

